bool result2 = valor1 != valor2;
Console.WriteLine("El resultado de la comparación valor1 != valor2 es: " + result2);

bool result3 = valor1 > valor2;
Console.WriteLine("El resultado de la comparación valor1 > valor2 es: " + result3);

bool result4 = valor1 < valor2;
Console.WriteLine("El resultado de la comparación valor1 < valor2 es: " + result4);

bool result5 = valor1 >= valor2;
Console.WriteLine("El resultado de la comparación valor1 >= valor2 es: " + result5);

bool result6 = valor1 <= valor2;
